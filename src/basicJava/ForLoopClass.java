package basicJava;

public class ForLoopClass {

	public static void main(String[] args) {
		//i++ means i = i+1;
		//1 to 10 Print
		//Initilization, condition, Increment
		for (int i = 1; i <=10; i++) {
			System.out.println(i);

		}

		//j-- means j = j-1;
		//10 to 1 Print
		//Initilization, condition, Decrement
		for (int j = 10 ; j >=1; j--) {
			System.out.println(j);

		}

	}

}
